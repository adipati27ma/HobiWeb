            <p><b>LINE</b> - adalah suatu aplikasi yang mungkin sekarang ini sedang populer di kalangan remaja hingga dewasa. LINE merupakan salah-satu sosial media yang sudah menyebar luas dan menjadi aplikasi sosial chat yang sangat populer khususnya di Indonesia. Dengan tampilan hijau khas-nya ini, LINE ini ternyata memiliki <strong>171 juta pengguna aktif bulanan</strong> pada tahun <strong>2017</strong> (dikutip dari <a href="https://www.liputan6.com/tekno/read/2997411/line-kantongi-171-juta-pengguna-aktif-bulanan">liputan6.com</a>) dan mengalami banyak sekali peningkatan pengguna di sepanjang tahun 2016 hingga 2017. Dan hingga tahun <strong>2018</strong>, pengguna LINE di Indonesia mencapai <strong>90 juta pengguna</strong> dan itu bukanlah angka yang sedikit.</p>
			<p align="center"><img class="img-fluid" src="img/KontenPost1/LogoLine.png" alt="Line Logo" title="Logo Line">
			<span class="caption text-muted">Logo Line</span></p>

            <p>Sosial media untuk chatting di saat sekarang ini sudah dapat dikatakan menjadi kebutuhan primer setiap orang dan hampir semua dari kita memiliki sosial media walaupun hanya satu atau dua saja. Semakin berkembangnya teknologi dan semakin mudahnya aplikasi sosial media untuk digunakan, maka penggunanya pun akan semakin mudah untuk mengakses dan menggunakannya. Terlebih kita merupakan makhluk sosial yang akan selalu berinteraksi dengan yang lainnya, dan dalam kasus ini seberapa jauh pun jaraknya tidak akan menjadi masalah untuk kita dapat berkomunikasi dengan kerabat kita baik kerabat jauh maupun kerabat dekat.</p>

            <p>Aplikasi LINE ini biasa kita temui di smartphone Android, memiliki banyak fitur yang mempermudah kita untuk berinteraksi, bahkan sekarang banyak fitur-fitur baru yang dapat memanjakan penggunanya. Ada pula games-games dari pihak LINE yang dapat  kita mainkan bersama teman LINE kita, tentu itu akan lebih asyik jika bisa dimainkan bersama. Jika orang tua memiliki aplikasi LINE pun sudah tidak aneh, karena di LINE juga ada fitur berita "LINE Today" yang selalu update setiap saat, lengkap, serta tertata dengan rapi sehingga nyaman untuk dilihat oleh mata.</p>

            <p>Nah, tapi sekarang kita akan bahas betapa fleksibelnya LINE ini karena telah menyediakan <em><b>PC version</b></em> yang bisa kita gunakan Komputer atau Laptop kita. Jadi untuk mengakses LINE di PC/Laptop, Anda tidak harus mengunduh simulator terlebih dahulu untuk dapat menginstallnya. Anda cukup mengunduh aplikasi <em>LINE for PC</em> dan menginstallnya, lalu menghubungkannya ke internet dengan cara login manual atau login dengan barcode. Anda tidak perlu cemas saat login di PC/Laptop, karena akun LINE yg aktif pada smartphone Anda tidak akan logout/hilang. Jadi Anda bisa menggunakan akun LINE yang sama di dua device yang berbeda, yakni di smartphone anda dan di PC/Laptop Anda.</p>
			
